"""
tests/test_stage2_parse.py
==========================
stage2_process._parse_axis_partners() の単体テスト。

検証方針（CHECK-9）:
  テスト期待値はすべて競輪予測テキストの実際のフォーマット仕様に基づく手計算で検証。
  「本命: X番 / 軸相手: Y番、Z番」は stage2_process.py ドキュメントで定義された公式フォーマット。

バグ修正確認:
  - 「3連単 4-6,2-6,2 ボックス」の 買い目 行数字を誤パースしないこと（DOTALL終端テスト）
  - partners 重複除去の確認
  - 軸番号が partners に含まれる場合の除外確認
"""

import sys
from pathlib import Path

import pytest

sys.path.insert(0, str(Path(__file__).resolve().parent.parent / "scripts"))
from stage2_process import _parse_axis_partners


# --------------------------------------------------------------------------- #
# 正常ケース: 標準複数行フォーマット
# --------------------------------------------------------------------------- #

def test_standard_multiline_format():
    """標準フォーマット（複数行）で正しく axis / partners を取得する。
    根拠: ドキュメント記載の公式フォーマット「本命: X番 / 軸相手: Y番、Z番 / 買い目: ...」
    手計算: 本命:4 → axis=4, 軸相手:6,2 → partners=[6,2]（重複なし・軸除外不要）
    """
    text = "本命: 4番（選手名A）\n軸相手: 6番、2番\n買い目: 3連単 4-6,2-6,2 ボックス\n根拠: ラインABC"
    axis, partners = _parse_axis_partners(text)
    assert axis == 4
    assert partners == [6, 2]


# --------------------------------------------------------------------------- #
# バグ再現テスト: 単行フォーマットで 買い目 行の数字を誤パースしない
# --------------------------------------------------------------------------- #

def test_single_line_buyme_not_leaked():
    """単行フォーマットでも 買い目 行の番号が partners に漏れない（バグ再現テスト）。
    旧パース: re.search(r"(?:軸相手|相手)[：:](.*)") → 「6,2番　買い目: 3連単 4-6,2-6,2」を全捕捉
                → partners=[6,2,3,4,6,2,6,2] になるバグ
    新パース: (?:\n|買い目|$) で終端 → 「6,2番　」のみ捕捉 → partners=[6,2]
    手計算: 相手欄は「6,2番　」→ [6,2]。買い目の数字は含まない。
    """
    text = "本命: 4番　軸相手: 6,2番　買い目: 3連単 4-6,2-6,2 ボックス"
    axis, partners = _parse_axis_partners(text)
    assert axis == 4
    assert partners == [6, 2]
    assert 3 not in partners  # 買い目の「3」連単が漏れていないこと


# --------------------------------------------------------------------------- #
# 重複パートナーの dedup 確認
# --------------------------------------------------------------------------- #

def test_partners_dedup():
    """重複する相手番号が dedup（順序保持）されること。
    手計算: 相手欄「6,2,6,2番」→ raw=[6,2,6,2] → dedup→ [6,2]（最初の出現を保持）
    """
    text = "本命: 4番\n軸相手: 6,2,6,2番\n買い目: 3連単 ながし"
    axis, partners = _parse_axis_partners(text)
    assert axis == 4
    assert partners == [6, 2]
    assert len(partners) == len(set(partners))  # 重複なし


# --------------------------------------------------------------------------- #
# 軸番号が相手欄に含まれる場合の除外
# --------------------------------------------------------------------------- #

def test_axis_excluded_from_partners():
    """軸番号が相手欄に混入していても除外されること。
    手計算: 相手欄「4,6,2番」→ raw=[4,6,2] → axis=4を除外 → [6,2]
    """
    text = "本命: 4番\n軸相手: 4,6,2番\n買い目: 3連単 ながし"
    axis, partners = _parse_axis_partners(text)
    assert axis == 4
    assert 4 not in partners
    assert partners == [6, 2]


# --------------------------------------------------------------------------- #
# フォールバック: 本命/軸キーワードなし
# --------------------------------------------------------------------------- #

def test_fallback_no_keyword():
    """本命/軸キーワードがない場合、全体から 1-7 の数字でフォールバック解析。
    手計算: テキスト「3連単 4-6-2 ながし」→ \b([1-7])\b の findall
            = ['4', '6', '2'] → axis=4, partners=[6,2]（dedup・軸除外済み）
    """
    text = "3連単 4-6-2 ながし"
    axis, partners = _parse_axis_partners(text)
    assert axis == 4
    assert partners == [6, 2]


# --------------------------------------------------------------------------- #
# 解析失敗: 数字なし
# --------------------------------------------------------------------------- #

def test_empty_or_no_numbers():
    """数字が全くないテキストでは (None, []) を返す。"""
    axis, partners = _parse_axis_partners("根拠のみのテキスト")
    assert axis is None
    assert partners == []


# --------------------------------------------------------------------------- #
# 相手番号最大4名制限
# --------------------------------------------------------------------------- #

def test_partners_max_four():
    """相手番号は最大4名に制限される。
    手計算: 相手欄「2,3,5,6,7番」→ raw=[2,3,5,6,7] → axis=1除外不要 → [:4]=[2,3,5,6]
    """
    text = "本命: 1番\n軸相手: 2,3,5,6,7番\n買い目: ながし"
    axis, partners = _parse_axis_partners(text)
    assert axis == 1
    assert len(partners) <= 4
    assert partners == [2, 3, 5, 6]
